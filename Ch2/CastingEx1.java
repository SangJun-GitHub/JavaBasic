public class CastingEx1 {
    public static void main(String[] args) {
        double d = 84.5;
        int i = (int)d;

        System.out.println("double d = " + d);  //84.5
        System.out.println("int i = " + i);     //84
        System.out.println("double d = " + d);  //84.5
    }
}
